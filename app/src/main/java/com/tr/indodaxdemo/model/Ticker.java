package com.tr.indodaxdemo.model;

public class Ticker {
  private String high;
  private String low;
  private String vol_idr;
  private String last;
  private String buy;
  private String sell;
  private String name;

  public String getHigh() {
    return high;
  }

  public void setHigh(String high) {
    this.high = high;
  }

  public String getLow() {
    return low;
  }

  public void setLow(String low) {
    this.low = low;
  }

  public String getVol_idr() {
    return vol_idr;
  }

  public void setVol_idr(String vol_idr) {
    this.vol_idr = vol_idr;
  }

  public String getLast() {
    return last;
  }

  public void setLast(String last) {
    this.last = last;
  }

  public String getBuy() {
    return buy;
  }

  public void setBuy(String buy) {
    this.buy = buy;
  }

  public String getSell() {
    return sell;
  }

  public void setSell(String sell) {
    this.sell = sell;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
